    private void jButton2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MousePressed
        //OBTENDO A QUANTIDADE ATUAL DE VOTOS EM BRANCO do campo votoBranco
        try{
            BrancoDAO dAO = new BrancoDAO();//Faz com que o método "carregarNomeCandidato" reconheça
            //que está chamando o método pesquisarCandidatoPorNumero
            //dentro da classe CandidatoDAO usando o parâmetro rs que está sendo retornado
            ResultSet rs = null;

            rs = dAO.obterVotosBrancos();

            carregarQuantidadeVotoBranco(rs);
        }
        catch(NumberFormatException ex){
            System.out.println("Erro: " + ex.getMessage());

        }

        //MANIPULANDO O BRANCO DE ACORDO COM A QUANTIDADE DO CAMPO votoBranco
        try{
            BrancoDAO dAO2 = new BrancoDAO();
            ResultSet rs = null;
            int votoBranco = 1;
            int codigo = 1;

            rs = dAO2.obterVotosBrancos();

            if(rs.next()) // se o ResultSet rs tiver algum nenhum resultado encontrado

            {
                //obtendo os dados da alteração
                BrancoDAO dDAO = new BrancoDAO();

                int votosTotaisBrancos = votoBrancoFiltrado;
                int votos = votosTotaisBrancos +1;

                dDAO.alterarQuantidadeVotoBranco(votos);
                JOptionPane.showMessageDialog(null, "Voto em branco sucedido!");
                txtNumeroCandidato.setText("");
                txtNomeCandidato.setText("");
            }
            else // se o ResultSet rs NÃO encontrar nenhum resultado.

            dAO2.incluirVotosBrancos(codigo, votoBranco);
        }
        catch(Exception ex){
            System.out.println("Erro: " + ex.getMessage());
        }
        txtNumeroCandidato.requestFocus();
    }//GEN-LAST:event_jButton2MousePressed

