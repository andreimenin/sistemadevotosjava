    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed

        try{
            CandidatoDAO dAO2 = new CandidatoDAO();
            ResultSet rs = null;

            rs = dAO2.pesquisarCandidatoPorNumero(Integer.parseInt(txtNumeroCandidato.getText()));

            if(rs.next()) // se o ResultSet rs tiver algum nenhum resultado encontrado

            {
                //obtendo os dados da alteração
                CandidatoDAO dDAO = new CandidatoDAO();

                int codigo = codigoFiltrado;
                int votosTotais = votoFiltrado;
                int votos = votosTotais +1;

                dDAO.atualizarVotos(codigo, votos);
                txtNumeroCandidato.setText("");
                txtNomeCandidato.setText("");
                txtNumeroCandidato.requestFocus();
            }
            else // se o ResultSet rs NÃO encontrar nenhum resultado.
            {
                if (JOptionPane.showConfirmDialog(null, "Tem certeza que deseja ANULAR o voto?", "ALERTA !",JOptionPane.YES_NO_OPTION)==1){
                return ;
                }
                else {
                
                //OBTENDO A QUANTIDADE ATUAL DE VOTOS EM NULO do campo votoNulo
                try{
                    NuloDAO dAO = new NuloDAO();//Faz com que o método "carregarNomeCandidato" reconheça
                    //que está chamando o método pesquisarCandidatoPorNumero
                    //dentro da classe CandidatoDAO usando o parâmetro rs que está sendo retornado
                    ResultSet rs1 = null;

                    rs1 = dAO.obterVotosNulos();

                    carregarQuantidadeVotoNulo(rs1);
                }
                catch(NumberFormatException ex){
                    System.out.println("Erro: " + ex.getMessage());

                }

                //MANIPULANDO O NULO DE ACORDO COM A QUANTIDADE DO CAMPO votoNulo
                try{
                    NuloDAO dAO1 = new NuloDAO();
                    ResultSet rs1 = null;
                    int votoNulo = 1;
                    int codigo = 1;

                    rs = dAO1.obterVotosNulos();

                    if(rs.next()) // se o ResultSet rs tiver algum nenhum resultado encontrado
                                //será apenas acrescentado, invés de ser criado um novo objeto "nulo"
                    {
                        //obtendo os dados da alteração
                        NuloDAO dDAO = new NuloDAO();

                        int votosTotaisNulos = votoNuloFiltrado;
                        int votos = votosTotaisNulos +1;

                        dDAO.alterarQuantidadeVotoNulo(votos);
                        JOptionPane.showMessageDialog(null, "Voto ANULADO com sucesso!");
                        txtNumeroCandidato.setText("");
                        txtNomeCandidato.setText("");
                        txtNumeroCandidato.requestFocus();
                    }
                    else //se o ResultSet rs NÃO encontrar nenhum resultado o objeto será criado e o voto adicionado.

                    dAO1.incluirVotosNulos(codigo, votoNulo);
                }
                catch(Exception ex){
                    System.out.println("Erro: " + ex.getMessage());
                }
            }
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Digite o número do candidato !\n\n Ou um número qualquer, caso queira anular o voto !");
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_jButton1MousePressed

